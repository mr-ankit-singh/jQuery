/*
#__ jQuery - Remove Elements __#

    With jQuery, it is easy to remove existing HTML elements.

 * Remove Elements/Content

    To remove elements and content, there are mainly two jQuery methods:

        remove() - Removes the selected element (and its child elements)
        empty() - Removes the child elements from the selected element


 1) jQuery remove() Method

    The jQuery remove() method removes the selected element(s) and its child elements.

    Example:
        $("#div1").remove();


 2) jQuery empty() Method

    The jQuery empty() method removes the child elements of the selected element(s).

    Example
        $("#div1").empty();


 3) jQuery datach() Method

   Removes selected elements(keep the data and events)


 4) removeattr() method

   remove one or more attribute from the selected element

 5) removeProp()

   Remove the property set the prop() method

*/